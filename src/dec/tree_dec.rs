pub unsafe extern "C" fn VP8ParseProba(br: *mut VP8BitReader, dec: *mut VP8Decoder) {
    let proba: *mut VP8Proba = &mut (*dec).proba_;
    let mut t: libc::c_int = 0;
    let mut b: libc::c_int = 0;
    let mut c: libc::c_int = 0;
    let mut p: libc::c_int = 0;
    t = 0 as libc::c_int;
    while t < NUM_TYPES as libc::c_int {
        b = 0 as libc::c_int;
        while b < NUM_BANDS as libc::c_int {
            c = 0 as libc::c_int;
            while c < NUM_CTX as libc::c_int {
                p = 0 as libc::c_int;
                while p < NUM_PROBAS as libc::c_int {
                    let v: libc::c_int = (if VP8GetBit(
                        br,
                        CoeffsUpdateProba[t as usize][b as usize][c as usize][p as usize]
                            as libc::c_int,
                    ) != 0
                    {
                        VP8GetValue(br, 8 as libc::c_int)
                    } else {
                        CoeffsProba0[t as usize][b as usize][c as usize][p as usize]
                            as libc::c_uint
                    }) as libc::c_int;
                    (*proba)
                        .bands_[t as usize][b as usize]
                        .probas_[c as usize][p as usize] = v as uint8_t;
                    p += 1;
                    p;
                }
                c += 1;
                c;
            }
            b += 1;
            b;
        }
        b = 0 as libc::c_int;
        while b < 16 as libc::c_int + 1 as libc::c_int {
            (*proba)
                .bands_ptr_[t
                as usize][b
                as usize] = &mut *(*((*proba).bands_).as_mut_ptr().offset(t as isize))
                .as_mut_ptr()
                .offset(*kBands.as_ptr().offset(b as isize) as isize)
                as *mut VP8BandProbas;
            b += 1;
            b;
        }
        t += 1;
        t;
    }
    (*dec).use_skip_proba_ = VP8GetValue(br, 1 as libc::c_int) as libc::c_int;
    if (*dec).use_skip_proba_ != 0 {
        (*dec).skip_p_ = VP8GetValue(br, 8 as libc::c_int) as uint8_t;
    }
}
