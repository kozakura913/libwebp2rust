unsafe extern "C" fn ParseIntraMode(
    br: *mut VP8BitReader,
    dec: *mut VP8Decoder,
    mut mb_x: libc::c_int,
) {
    let top: *mut uint8_t = ((*dec).intra_t_).offset((4 as libc::c_int * mb_x) as isize);
    let left: *mut uint8_t = ((*dec).intra_l_).as_mut_ptr();
    let block: *mut VP8MBData = ((*dec).mb_data_).offset(mb_x as isize);
    if (*dec).segment_hdr_.update_map_ != 0 {
        (*block)
            .segment_ = (if VP8GetBit(
            br,
            (*dec).proba_.segments_[0 as libc::c_int as usize] as libc::c_int,
        ) == 0
        {
            VP8GetBit(
                br,
                (*dec).proba_.segments_[1 as libc::c_int as usize] as libc::c_int,
            )
        } else {
            VP8GetBit(
                br,
                (*dec).proba_.segments_[2 as libc::c_int as usize] as libc::c_int,
            ) + 2 as libc::c_int
        }) as uint8_t;
    } else {
        (*block).segment_ = 0 as libc::c_int as uint8_t;
    }
    if (*dec).use_skip_proba_ != 0 {
        (*block).skip_ = VP8GetBit(br, (*dec).skip_p_ as libc::c_int) as uint8_t;
    }
    (*block)
        .is_i4x4_ = (VP8GetBit(br, 145 as libc::c_int) == 0) as libc::c_int as uint8_t;
    if (*block).is_i4x4_ == 0 {
        let ymode: libc::c_int = if VP8GetBit(br, 156 as libc::c_int) != 0 {
            if VP8GetBit(br, 128 as libc::c_int) != 0 {
                TM_PRED as libc::c_int
            } else {
                H_PRED as libc::c_int
            }
        } else if VP8GetBit(br, 163 as libc::c_int) != 0 {
            V_PRED as libc::c_int
        } else {
            DC_PRED as libc::c_int
        };
        (*block).imodes_[0 as libc::c_int as usize] = ymode as uint8_t;
        memset(
            top as *mut libc::c_void,
            ymode,
            (4 as libc::c_int as libc::c_ulong)
                .wrapping_mul(::core::mem::size_of::<uint8_t>() as libc::c_ulong),
        );
        memset(
            left as *mut libc::c_void,
            ymode,
            (4 as libc::c_int as libc::c_ulong)
                .wrapping_mul(::core::mem::size_of::<uint8_t>() as libc::c_ulong),
        );
    } else {
        let mut modes: *mut uint8_t = ((*block).imodes_).as_mut_ptr();
        let mut y: libc::c_int = 0;
        y = 0 as libc::c_int;
        while y < 4 as libc::c_int {
            let mut ymode_0: libc::c_int = *left.offset(y as isize) as libc::c_int;
            let mut x: libc::c_int = 0;
            x = 0 as libc::c_int;
            while x < 4 as libc::c_int {
                let prob: *const uint8_t = (kBModesProba[*top.offset(x as isize)
                    as usize][ymode_0 as usize])
                    .as_ptr();
                let mut i: libc::c_int = kYModesIntra4[VP8GetBit(
                    br,
                    *prob.offset(0 as libc::c_int as isize) as libc::c_int,
                ) as usize] as libc::c_int;
                while i > 0 as libc::c_int {
                    i = kYModesIntra4[(2 as libc::c_int * i
                        + VP8GetBit(br, *prob.offset(i as isize) as libc::c_int))
                        as usize] as libc::c_int;
                }
                ymode_0 = -i;
                *top.offset(x as isize) = ymode_0 as uint8_t;
                x += 1;
                x;
            }
            memcpy(
                modes as *mut libc::c_void,
                top as *const libc::c_void,
                (4 as libc::c_int as libc::c_ulong)
                    .wrapping_mul(::core::mem::size_of::<uint8_t>() as libc::c_ulong),
            );
            modes = modes.offset(4 as libc::c_int as isize);
            *left.offset(y as isize) = ymode_0 as uint8_t;
            y += 1;
            y;
        }
    }
    (*block)
        .uvmode_ = (if VP8GetBit(br, 142 as libc::c_int) == 0 {
        DC_PRED as libc::c_int
    } else if VP8GetBit(br, 114 as libc::c_int) == 0 {
        V_PRED as libc::c_int
    } else if VP8GetBit(br, 183 as libc::c_int) != 0 {
        TM_PRED as libc::c_int
    } else {
        H_PRED as libc::c_int
    }) as uint8_t;
}
