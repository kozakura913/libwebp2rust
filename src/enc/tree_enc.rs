pub unsafe extern "C" fn VP8WriteProbas(
    bw: *mut VP8BitWriter,
    probas: *const VP8EncProba,
) {
    let mut t: libc::c_int = 0;
    let mut b: libc::c_int = 0;
    let mut c: libc::c_int = 0;
    let mut p: libc::c_int = 0;
    t = 0 as libc::c_int;
    while t < NUM_TYPES as libc::c_int {
        b = 0 as libc::c_int;
        while b < NUM_BANDS as libc::c_int {
            c = 0 as libc::c_int;
            while c < NUM_CTX as libc::c_int {
                p = 0 as libc::c_int;
                while p < NUM_PROBAS as libc::c_int {
                    let p0: uint8_t = (*probas)
                        .coeffs_[t as usize][b as usize][c as usize][p as usize];
                    let update: libc::c_int = (p0 as libc::c_int
                        != VP8CoeffsProba0[t
                            as usize][b as usize][c as usize][p as usize] as libc::c_int)
                        as libc::c_int;
                    if VP8PutBit(
                        bw,
                        update,
                        VP8CoeffsUpdateProba[t
                            as usize][b as usize][c as usize][p as usize] as libc::c_int,
                    ) != 0
                    {
                        VP8PutBits(bw, p0 as uint32_t, 8 as libc::c_int);
                    }
                    p += 1;
                    p;
                }
                c += 1;
                c;
            }
            b += 1;
            b;
        }
        t += 1;
        t;
    }
    if VP8PutBitUniform(bw, (*probas).use_skip_proba_) != 0 {
        VP8PutBits(bw, (*probas).skip_proba_ as uint32_t, 8 as libc::c_int);
    }
}
