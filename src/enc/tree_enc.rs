pub unsafe extern "C" fn VP8CodeIntraModes(enc: *mut VP8Encoder) {
    let bw: *mut VP8BitWriter = &mut (*enc).bw_;
    let mut it: VP8EncIterator = VP8EncIterator {
        x_: 0,
        y_: 0,
        yuv_in_: 0 as *mut uint8_t,
        yuv_out_: 0 as *mut uint8_t,
        yuv_out2_: 0 as *mut uint8_t,
        yuv_p_: 0 as *mut uint8_t,
        enc_: 0 as *mut VP8Encoder,
        mb_: 0 as *mut VP8MBInfo,
        bw_: 0 as *mut VP8BitWriter,
        preds_: 0 as *mut uint8_t,
        nz_: 0 as *mut uint32_t,
        i4_boundary_: [0; 37],
        i4_top_: 0 as *mut uint8_t,
        i4_: 0,
        top_nz_: [0; 9],
        left_nz_: [0; 9],
        bit_count_: [[0; 3]; 4],
        luma_bits_: 0,
        uv_bits_: 0,
        lf_stats_: 0 as *mut LFStats,
        do_trellis_: 0,
        count_down_: 0,
        count_down0_: 0,
        percent0_: 0,
        left_derr_: [[0; 2]; 2],
        top_derr_: 0 as *mut DError,
        y_left_: 0 as *mut uint8_t,
        u_left_: 0 as *mut uint8_t,
        v_left_: 0 as *mut uint8_t,
        y_top_: 0 as *mut uint8_t,
        uv_top_: 0 as *mut uint8_t,
        yuv_left_mem_: [0; 88],
        yuv_mem_: [0; 3359],
    };
    VP8IteratorInit(enc, &mut it);
    loop {
        let mb: *const VP8MBInfo = it.mb_;
        let mut preds: *const uint8_t = it.preds_;
        if (*enc).segment_hdr_.update_map_ != 0 {
            PutSegment(
                bw,
                (*mb).segment_() as libc::c_int,
                ((*enc).proba_.segments_).as_mut_ptr(),
            );
        }
        if (*enc).proba_.use_skip_proba_ != 0 {
            VP8PutBit(
                bw,
                (*mb).skip_() as libc::c_int,
                (*enc).proba_.skip_proba_ as libc::c_int,
            );
        }
        if VP8PutBit(
            bw,
            ((*mb).type_() as libc::c_int != 0 as libc::c_int) as libc::c_int,
            145 as libc::c_int,
        ) != 0
        {
            PutI16Mode(bw, *preds.offset(0 as libc::c_int as isize) as libc::c_int);
        } else {
            let preds_w: libc::c_int = (*enc).preds_w_;
            let mut top_pred: *const uint8_t = preds.offset(-(preds_w as isize));
            let mut x: libc::c_int = 0;
            let mut y: libc::c_int = 0;
            y = 0 as libc::c_int;
            while y < 4 as libc::c_int {
                let mut left: libc::c_int = *preds.offset(-(1 as libc::c_int) as isize)
                    as libc::c_int;
                x = 0 as libc::c_int;
                while x < 4 as libc::c_int {
                    let probas: *const uint8_t = (kBModesProba[*top_pred
                        .offset(x as isize) as usize][left as usize])
                        .as_ptr();
                    left = PutI4Mode(
                        bw,
                        *preds.offset(x as isize) as libc::c_int,
                        probas,
                    );
                    x += 1;
                    x;
                }
                top_pred = preds;
                preds = preds.offset(preds_w as isize);
                y += 1;
                y;
            }
        }
        PutUVMode(bw, (*mb).uv_mode_() as libc::c_int);
        if !(VP8IteratorNext(&mut it) != 0) {
            break;
        }
    };
}
