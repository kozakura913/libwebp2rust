unsafe extern "C" fn VP8BitCost(
    mut bit: libc::c_int,
    mut proba: uint8_t,
) -> libc::c_int {
    return if bit == 0 {
        VP8EntropyCost[proba as usize] as libc::c_int
    } else {
        VP8EntropyCost[(255 as libc::c_int - proba as libc::c_int) as usize]
            as libc::c_int
    };
}
